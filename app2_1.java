// app2_1, 簡單的Java程式
public class app2_1   // 定義public 類別app2_1
{
   public static void main(String args[])  // main() method, 主程式開始
   {
      int num;       // 宣告整數num
      num=2;         // 將num設值為2
      System.out.println("I have "+num+" dogs");   // 印出字串及變數內容
      System.out.println("You have "+num+" dogs, too");
   }
}